package a40009_imperial.junitinterop03.q03;

public final class ExamLocation {
    private final int x;
    private final int y;

    public ExamLocation(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
